def main() -> None:
    parser = argparse.ArgumentParser(description="Bootstrap SSVEP filter taps and NN weights.")
    parser.add_argument("--mat", type=Path, help="Optional path to one data_sX_64.mat file")
    parser.add_argument("--mat-dir", type=Path, help="Directory containing one or more data_sX_64.mat files")
    parser.add_argument("--channels-csv", type=Path, help="Optional path to Electrode_channels_information.csv")
    parser.add_argument("--channel-indices", default="56,58,60", help="Fallback 0-based O1/Oz/O2 indices")
    parser.add_argument("--detector-fundamentals", default="10,12,15,20", help="Comma-separated detector fundamentals for the 12 input features")
    parser.add_argument("--label-fundamentals", default="10,12,15,20", help="Comma-separated label fundamentals to classify")
    parser.add_argument("--condition", choices=["low", "high", "both"], default="high")
    parser.add_argument("--spatial-q16", default="-32768,65536,-32768", help="Default zero-sum spatial prior")
    parser.add_argument("--data-scale", type=float, default=0.0, help="Replay scale; <= 0 enables automatic scale selection")
    parser.add_argument("--scale-percentile", type=float, default=99.5, help="Percentile used for automatic scale selection")
    parser.add_argument("--scale-headroom", type=float, default=0.85, help="Headroom target for automatic Q18 scaling")
    parser.add_argument("--holdout-count", type=int, default=6, help="Number of subject files to hold out for evaluation")
    parser.add_argument("--holdout-seed", type=int, default=1337, help="Deterministic seed for subject holdout split")
    parser.add_argument("--epochs", type=int, default=180)
    parser.add_argument("--lr", type=float, default=3e-4)
    parser.add_argument("--batch-size", type=int, default=128)
    parser.add_argument("--dropout", type=float, default=0.0, help="Dropout probability between hidden layers")
    parser.add_argument("--label-smoothing", type=float, default=0.0, help="Label smoothing epsilon for CrossEntropyLoss")
    parser.add_argument("--workers", type=int, default=max(1, min(4, os.cpu_count() or 1)))
    parser.add_argument("--trainer", choices=["auto", "numpy", "torch"], default="auto")
    parser.add_argument("--seed", type=int, default=1337)
    parser.add_argument("--report-name", default="metrics.json", help="Filename for the JSON report")
    parser.add_argument("--outdir", type=Path, default=Path("."))
    parser.add_argument("--emit-defaults", action="store_true", help="Skip dataset loading and emit deterministic default headers")
    args = parser.parse_args()

    detector_fundamentals = parse_freq_list(args.detector_fundamentals)
    label_fundamentals = parse_freq_list(args.label_fundamentals)
    nyquist_hz = RTL_FS // 2
    # ── Fundamentals-first dedup ──────────────────────────────────────
    # Process ALL fundamentals first (h=1), then ALL 2nd harmonics (h=2),
    # then ALL 3rd harmonics (h=3).  This guarantees every class keeps
    # its fundamental frequency — the strongest SSVEP component.
    # Also avoids 50 Hz (killed by the frontend notch filter).
    NOTCH_FREQ = 50  # notch filter centre frequency
    seen: set[int] = set()
    # allocations[i] will hold the 3 detector freqs for fundamental i
    allocations: dict[int, list[int]] = {base: [] for base in detector_fundamentals}

    for h in (1, 2, 3):                          # harmonic level
        for base in detector_fundamentals:        # each fundamental
            freq = base * h
            if freq not in seen and freq != NOTCH_FREQ and freq < nyquist_hz:
                seen.add(freq)
                allocations[base].append(freq)
            else:
                # Find the lowest unique harmonic for this fundamental
                replaced = False
                for alt_h in range(h + 1, 2 * nyquist_hz // base + 1):
                    candidate = base * alt_h
                    if (candidate < nyquist_hz and
                            candidate not in seen and
                            candidate != NOTCH_FREQ):
                        seen.add(candidate)
                        allocations[base].append(candidate)
                        reason = "notch" if freq == NOTCH_FREQ else "collision"
                        print(
                            f"[dedup] {base}x{h}={freq} Hz ({reason}); "
                            f"replaced with {base}x{alt_h}={candidate} Hz"
                        )
                        replaced = True
                        break
                if not replaced:
                    raise SystemExit(
                        f"Cannot find a unique harmonic for fundamental {base} Hz "
                        f"(all harmonics below {nyquist_hz} Hz already used)"
                    )

    # Flatten in canonical order: all features for base[0], then base[1], …
    detector_freqs: list[int] = []
    for base in detector_fundamentals:
        detector_freqs.extend(allocations[base])
    print(f"[dedup] Final detector bank: {detector_freqs}")
    if len(detector_freqs) != FEATURE_COUNT:
        raise SystemExit(f"detector fundamentals must expand to exactly {FEATURE_COUNT} features")

    args.outdir.mkdir(parents=True, exist_ok=True)
    filter_header = args.outdir / "filter_coeffs.vh"
    nn_header = args.outdir / "neural_network_weights.vh"
    report_path = args.outdir / args.report_name

    if args.emit_defaults:
        emit_filter_header(filter_header, detector_freqs)
        emit_weight_header(nn_header, *default_weight_pack(args.seed))
        print(f"[ok] emitted deterministic default headers to {args.outdir}")
        return

    mat_paths = collect_mat_paths(args.mat, args.mat_dir)
    if args.channels_csv:
        channel_indices = load_channel_indices(args.channels_csv, ["O1", "OZ", "O2"])
    else:
        channel_indices = parse_int_list(args.channel_indices, 3, "channel-indices")

    spatial_q16 = parse_int_list(args.spatial_q16, 3, "spatial-q16")
    workers = max(1, min(int(args.workers), len(mat_paths)))
    train_paths, holdout_paths = split_subject_paths(mat_paths, args.holdout_count, args.holdout_seed)

    x_train_raw, y_train, groups_train = build_feature_set(
        mat_paths=train_paths,
        label_fundamentals=label_fundamentals,
        detector_fundamentals=detector_fundamentals,
        channel_indices=channel_indices,
        condition_mode=args.condition,
        spatial_q16=spatial_q16,
        workers=workers,
    )

    x_holdout_raw = None
    y_holdout = None
    groups_holdout: List[str] = []
    if holdout_paths:
        x_holdout_raw, y_holdout, groups_holdout = build_feature_set(
            mat_paths=holdout_paths,
            label_fundamentals=label_fundamentals,
            detector_fundamentals=detector_fundamentals,
            channel_indices=channel_indices,
            condition_mode=args.condition,
            spatial_q16=spatial_q16,
            workers=workers,
        )

    if args.data_scale > 0:
        train_scale = float(args.data_scale)
    else:
        train_scale = choose_scale(x_train_raw, args.scale_percentile, args.scale_headroom)

    x_train = apply_scale(x_train_raw, train_scale)
    train_feature_stats = describe_scaled_features(x_train_raw, train_scale)
    print(f"Training scale: {train_scale:.6f}")
    print(
        f"Training feature stats: min={train_feature_stats['min']:.1f} "
        f"max={train_feature_stats['max']:.1f} sat={train_feature_stats['q18_saturation_fraction']*100.0:.2f}%"
    )

    holdout_feature_stats = None
    x_holdout = None
    if x_holdout_raw is not None:
        x_holdout = apply_scale(x_holdout_raw, train_scale)
        holdout_feature_stats = describe_scaled_features(x_holdout_raw, train_scale)
        print(
            f"Holdout feature stats: min={holdout_feature_stats['min']:.1f} "
            f"max={holdout_feature_stats['max']:.1f} sat={holdout_feature_stats['q18_saturation_fraction']*100.0:.2f}%"
        )

    class_count = len(label_fundamentals)
    holdout_model = train_network(
        x=x_train,
        y=y_train,
        class_count=class_count,
        epochs=args.epochs,
        lr=args.lr,
        seed=args.seed,
        batch_size=args.batch_size,
        trainer=args.trainer,
        dropout=args.dropout,
        label_smoothing=args.label_smoothing,
    )

    train_metrics = evaluate_model(holdout_model, x_train, y_train, groups_train)
    holdout_metrics = None
    if x_holdout is not None and y_holdout is not None:
        holdout_metrics = evaluate_model(holdout_model, x_holdout, y_holdout, groups_holdout)
        print(
            f"Holdout accuracy: windows={holdout_metrics['window_accuracy']:.4f} "
            f"trials={holdout_metrics['trial_accuracy']:.4f}"
        )

    if x_holdout_raw is not None and y_holdout is not None:
        np = import_numpy()
        x_full_raw = np.concatenate([x_train_raw, x_holdout_raw], axis=0).astype(np.float32)
        y_full = np.concatenate([y_train, y_holdout], axis=0).astype(np.int64)
        groups_full = list(groups_train) + list(groups_holdout)
    else:
        x_full_raw = x_train_raw
        y_full = y_train
        groups_full = groups_train

    if args.data_scale > 0:
        full_scale = float(args.data_scale)
    else:
        full_scale = choose_scale(x_full_raw, args.scale_percentile, args.scale_headroom)

    x_full = apply_scale(x_full_raw, full_scale)
    full_feature_stats = describe_scaled_features(x_full_raw, full_scale)
    print(
        f"Final full-data scale: {full_scale:.6f}  "
        f"sat={full_feature_stats['q18_saturation_fraction']*100.0:.2f}%"
    )

    final_model = train_network(
        x=x_full,
        y=y_full,
        class_count=class_count,
        epochs=args.epochs,
        lr=args.lr,
        seed=args.seed,
        batch_size=args.batch_size,
        trainer=args.trainer,
        dropout=args.dropout,
        label_smoothing=args.label_smoothing,
    )

    final_metrics = evaluate_model(final_model, x_full, y_full, groups_full)
    hardware_model = pad_model_to_hardware_outputs(final_model)

    emit_filter_header(filter_header, detector_freqs)
    emit_weight_header(nn_header, *model_to_lists(hardware_model))

    report = experiment_report(
        name=args.outdir.name,
        train_subjects=train_paths,
        holdout_subjects=holdout_paths,
        label_fundamentals=label_fundamentals,
        detector_fundamentals=detector_fundamentals,
        condition_mode=args.condition,
        train_feature_stats=train_feature_stats,
        holdout_feature_stats=holdout_feature_stats,
        full_feature_stats=full_feature_stats,
        train_metrics=train_metrics,
        holdout_metrics=holdout_metrics,
        full_metrics=final_metrics,
    )
    report_path.write_text(json.dumps(report, indent=2), encoding="utf-8")

    print(f"[ok] wrote {filter_header}")
    print(f"[ok] wrote {nn_header}")
    print(f"[ok] wrote {report_path}")


if __name__ == "__main__":
    main()
