#!/usr/bin/env python3
"""
bootstrap_network.py

Offline bootstrap utility for the standalone FPGA SSVEP stack.

What this script does:
1. Loads one or more segmented MATLAB 7.3 subject files from the 1-60 Hz dataset.
2. Replays the 3-channel O1 / Oz / O2 front-end approximately in software.
3. Extracts the 12 coherent detector features used by the FPGA.
4. Supports subject-holdout evaluation for an honest offline accuracy number.
5. Trains either a 4-class extended model or a 2-class 15-vs-20 demo model.
6. Emits Vivado-ready Verilog headers, padding 2-class output layers to 4 logits.

The training path keeps the feature data in floating point as long as possible,
then only quantizes at header emission time. That keeps more of the dataset's
real structure intact while still giving you synthesizable fixed-point weights.
"""

from __future__ import annotations

